package messagers.util.chat.room;

public class ChatRoomSizeShower {

	private String name;
	private int actual;
	private int max;

	public String getName() {
		return name;
	}

	public void setName(final String name) {
		this.name = name;
	}

	public int getActual() {
		return actual;
	}

	public void setActual(final int actual) {
		this.actual = actual;
	}

	public int getMax() {
		return max;
	}

	public void setMax(final int max) {
		this.max = max;
	}

}
